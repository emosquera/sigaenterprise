package sigaenterprise.backend.auth.facade;

import sigaenterprise.backend.auth.model.Role;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;

/**
 *
 * @author syslife02
 */
@Stateless
public class RoleFacade extends AbstractFacade<Role> implements RoleFacadeLocal {

    @PersistenceContext(unitName = "sigaenterprise_backend_PU")
    private EntityManager em;

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }

    public RoleFacade() {
        super(Role.class);
    }

    @Override
    public Role findByRole(String role) {
        return (Role) em.createNamedQuery("findByRole").setParameter("role", role).getSingleResult();
    }
    
}
